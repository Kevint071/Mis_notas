nombre = "kevin"

# Para poner la primera letra del string en mayúscula se usa el método capitalize() o el método title()

nombre = nombre.capitalize()
print(nombre) # Kevin

nombre = nombre.title()
print(nombre) # Kevin

# Para poner todas las letras del string en mayúsculas se usa el método upper()

nombre = nombre.upper()
print(nombre) # KEVIN

# Para poner todas las letras del string en minúsculas se usa el metodo lower()

nombre = nombre.lower()
print(nombre) # kevin
